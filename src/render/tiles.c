void tiles_load_room_bg(void) {
    set_bkg_data(BGT_VOID,  1, bg_tile_void);
    set_bkg_data(BGT_FLOOR, 1, bg_tile_floor);
    set_bkg_data(BGT_WALL,  1, bg_tile_wall);
    set_bkg_data(BGT_DOOR,  1, bg_tile_door);
}

